"""Виведіть у порядку зростання усі повні куби натуральних чисел, що менші за n.

Вхідні дані
Одне натуральне число n (n > 1).

Вихідні дані
Виведіть в одному рядку усі повні куби натуральних чисел, що менші за n, в порядку зростання."""
n = int(input())
for i in range(1, n + 1):
    cube = i ** 3
    if cube < n:
        print(cube, end=" ")
    else:
        break

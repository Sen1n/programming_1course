'''Задано натуральне число m. Якщо це не паліндром, то записуємо його
у зворотному порядку та додаємо до заданого. Кроки повторюються, доки не буде отримано число-паліндром.
Кількість виконаних операцій назвемо рівнем паліндромності заданого числа.

Знайти рівень паліндромності числа m.'''

m = int(input())
palindrome_level = 0

while str(m) != str(m)[::-1]:
    reversed_m = int(str(m)[::-1])
    m += reversed_m
    palindrome_level += 1

print(palindrome_level)

"""На вході програми маємо послідовність цілих чисел, що закінчується числом 0. Потрібно
знайти суму даної послідовності, не враховуючи останнього нуля.

Вхідні дані
Послідовність цілих чисел, по одному числу в кожному рядку.

Вихідні дані
Одне число - суму даної послідовності."""
summ = 0
while True:
    n = int(input())
    if n != 0:
        summ += n
    else:
        break
print(summ)
